                            <b>At the end of a strafe</b>. I use it and it works. Especially with a&nbsp;<b>semi circle strafe</b>. It allows my hit box to be harder to hit (minecraft hit boxes are the derpiest thing ever) whilst dealing extra damage. I also use it so I can go back around the&nbsp;<b>semi circle strafe</b>.<br>
                          </li>
                          <li>
                            <b>When charging head on at a player</b>. Either jump over them or to the side. Whilst often predictable and over used due to minecraft hit boxes it works really well.&nbsp;<br>
                          </li>
                          <li>
                            <b>When on the back foot</b>. This just allows you to make static gameplay whilst dealing a bit of extra damage. Usually when you know you're going to lose but want to give them some extra damage just so your teammates can pick them off.
                          </li>
                        </ul>
                        <b>Archery</b>:&nbsp;Basically using your bow.<br><br>
                        <ul>
                          <li>
                            <b>Don't pull all the way back in close situations</b>. You don't need to and you get more arrows into your enemy whilst keeping them back.</li></ul><ul><li>
                            <b>When running use a bow</b>. When running back and an enemy is running after you, jump, turn around and put a shot into them. This knocks them back and flinches then for a fraction of a second, in this you can turn the fight around and combo them to death.</li></ul><ul><li>
                            <b>Machine gun</b>. When low just bow spam them to crap. I usually do this when I'm on 5 or 4 hearts as it gives me time to regen whilst keeping them back. Basically just tap your right click a bit (hold down for a fraction of a second) and this will maintain distance between you and your opponent.</li></ul><ul><li>
                            <b>Matsuzen tactic</b>. (I don't think Matsuzen invented it but he is the first person I saw use it and it works really well). Just before a fight either run away and shoot them or just shoot them. Put a couple of shots into them and then melee fight them. This lowers damage to you and makes the fight shorter.</li></ul><ul><li>
                            <b>Practice</b>. Play on servers such as Minez or RMCT where your bow pvp will improve massively and you learn more.&nbsp;<b>Bow pvp is a lot more about practice and experience than guides</b>. Here is a video what could help with practice too.&nbsp;<a href="http://www.youtube.com/watch?v=E1ulNMIsfQQ">http://www.youtube.com/watch?v=E1ulNMIsfQQ</a>
                          </li>
                        </ul>
                        <ul>
                          <li>
                            <b>Bow battles&nbsp;</b>- First thing to remember, is that the rate of fire and your accuracy are crucial to win duels. It is always better, to get in close enough for you to fire quicker shots then far away, where one can easily miss or have their shot dodged. Quicker shots will give you a better chance of damaging your opponent, in the same concept as fighting a swordsman. However, the enemy can return the fire too this time. Firing an arrow will greatly slow you down. This is an archer's most vulnerable point, and is the point you must take to your advantage when attacking another archer. Never linger on a shot, or concentrate too much on an aim. Fire your arrow and immediately move a few blocks to the left or right. Take another quick shot and move again. A master archer is able to hit an enemy and then dodge the enemy arrow and return fire immediately. Shoot while the enemy is charging his bow, then dodge once he releases the bowstring, and immediately charge your bow and take a shot again. Every hit on the enemy will knock them back, messing up their aim, every hit on you will do the same, possibly breaking your shoot-and-dodge pattern. Never hold the string for too long, for you are greatly slowed and that is when you cannot dodge the enemy arrow. Speed will always beat power.</li></ul><b>Special Tactics (<i>Archery)</i></b>:<br><ul><li>
                            <i><b>Fire Arrows</b></i>: When you shoot an arrow through a lava curtain, the arrow will become a fire arrow. This can light an opposing player on fire if the arrow hits them. Fire will drain their health very quickly and slow them down, allowing you to get easier shots on them. This tactic can be extremely useful to pressure the opposing team on certain maps such as RFV2 or RFV.</li></ul><ul><li>
                            <i><b>Shooting through water</b></i><b>:</b>&nbsp;Water significantly slows down the speed of&nbsp;arrows, causing it to dip downward a little bit. When shooting at a target underwater, aim a little higher than usual. The lower the target is underwater, the higher you should aim in order to hit them.</li></ul><ul><li>
                            <i><b>Interaction with blocks</b></i><b>:</b>&nbsp;Arrows can activate wooden pressure plates, set off wooden buttons and destroy boats. These can be extremely useful tactics in many maps on Overcast.&nbsp;Shooting wooden pressure plates can set off TNT on the enemy side, especially useful if someone is trying to Icarus. Many people also use boats to rush maps, and shooting their boats will render them extremely vulnerable in the water. Shooting wooden buttons is possibly the hardest out of the 3 but if you land the shot can be the hardest to counter. The only time you'd be shooting would be to counter a cannon. It is possible as I did it in a scrim against Bolt and managed to destroy their Whiskey cannon. The only time you'll usually be shooting at buttons are on cannons similar to whiskeys (unless you approach them from behind). Using arrows on cannons (wooden objects) can be extremely useful and can often leave the cannoneer amazed and wondering what just happened which stops them from preventing the damage their TNT will cause.
                          </li>
                        </ul>
                        <br>
                        <b>Other things</b>:
                        <br>
                        <br>
                        <ul>
                          <li>
                            <b>Try Blitz</b>. Blitz (chaos/cronus) means you only have 1 life per match (except a few maps), this means you have a smarter playstyle and be more conservative in what you do. It allows you to keep a cool head and analyse situations faster whilst being under pressure. Situations such as being outnumbered called for a quick and speedy mind whilst being able to find innovative ideas to overcome your opponents and put them into action effectively.<b><br></b>
                          </li>
                          <li>
                            <b>Rage (Gamemode)</b>. Rage is the exact same as&nbsp;<b>Blitz</b>&nbsp;however it is 1 hit/shot 1 kill. This means you have to be super wary, have fast reflexes and be accurate. Lacking in any of these means you will most likely die. Ping in sword combat can be ping orientated yes but if you play evasively (jumping to side, strafe) you usually come out on top. Bow is the best and most OP (Over Powered) weapon in Rage. It allows for long distance kills without causing much threat to yourself. Don't be hasty, take your time because a slip up means you're out. Don't go for a long range shot because it is highly unlikely to land. If you want to improve patience, reflex, awareness and accuracy Rage is a great gamemode.<br>
                          </li>
                          <li>
                            <b>Be accurate</b>. Look at reflex games, accuracy games. It'll increase it and you find yourself not having to click as fast to kill your enemy.
                          </li>
                          <li>
                            <b>Click fast</b>. Only use this if you have a generally OK accuracy or if you want to go crazy. Clicking fast gets rid of accuracy a bit but some can do both well and efficiently. Try something called jitter clicking, I can't really explain it but here's a video to show what it is.&nbsp;<a href="http://www.youtube.com/watch?v=bRGL2KBIPyM">http://www.youtube.com/watch?v=bRGL2KBIPyM</a><br>
                          </li>
                          <li>
                            <b>Countering Bowspam</b>.&nbsp;Move side to side in unpredictable movements. This confuses your opponent and makes the archer unable to focus you. Never stand in the same line for for than 1 second and never repeat your movements. This can mean jumping and side movements. As long as your general direction is forward, be as random as possible!<br>
                          </li>
                          <li>
                            <b>Using terrain</b>. When using terrain you need to remember certain things. If your enemy is close to the edge, try to knock them towards it and they will most likely fall off. If it goes downwards try to hit them towards it as they take extra damage from fall damage. If below them just side strafe, press a then d and repeat, you get an advantage from being below your opponent and moving side to side makes your hit box even harder to get (I have never been hit whilst doing this). To counter someone below you just jump over them, it's simple, and then they lose the advantage and are taken off guard. When using a bow try to get high ground, high ground gives you a HUGE advantage in archery.<br>
                          </li>
                          <li>
                            <b>Stay away from ladders</b>. I mean if you have to go up somewhere, sure but if you're fighting someone stay away from them. This is because when you go on the ladder the hit box of the ladder takes up a whole block. Your minecraft body goes inside the block and you just end up hitting the ladder. Your hit box stays outside the block so people can hit you still. Basically you can't hit them but they can hit you. If possibly knock them onto a ladder and it will be a LOT easier to kill them.<br>
                          </li>
                          <li>
                            <b>Eating Golden Apples</b>. If low on health and you have little risk of being in a battle again, do not eat your golden apples immediately afterwards. If you do you get around 6/5 seconds of regeneration 2, if you wait for your previous apple to wear down you can get 10 seconds (5 seconds of regeneration 2 per apple)&nbsp;of regeneration 2. It heals you 4 hearts per apple and gives you an additional 2 hearts. Due to this you can get 8 hearts instead of 6/5.<br>
                          </li>
                          <li>
                            <b>Finding the right FOV</b>. This can help a lot and people switch around a lot. Finding the right FOV helps you to concentrate and see more. Generally people either go for Normal, 90 or Quake Pro. The higher the FOV the more you can see; here is a comparison of Normal and Quake Pro&nbsp;<a href="http://imgur.com/RLWOkLc,ZIGk6wg#1">http://imgur.com/RLWOkLc,ZIGk6wg#1</a>. As you can clearly see I am able to see more with Quake Pro, however in order to do this the objects become smaller to the eye. People who bow often use Normal and people who use sword often pick 90. Mess around, you can always find a setting you like. Personally I've switched between 4 different FOVs, Quake Pro, 100, 90 and Normal (Currently using Normal). It can help with accuracy too as it is something what your eye is most comfortable with and you can foresee things easier.<br>
                          </li>
                          <li>
                            <b>Control your anger</b>. Anger can be an up and a down. It can help you to get motivated and you generally perform better if you get motivated. However if you get angry because of a negative reason (which it generally is), stop. It makes you worse of a player, knocks your confidence and lowers your teamwork abilities. It also knocks your senses needed in pvp. Everyone does it and will do it but try your best to avoid it. Stop, take a break, maybe join observers. Do something to distract yourself or analyse what happened, find a way to improve yourself and overcome it. Whilst it is hard because your frustrated if you pull it off, you will be able to have a broader mind set when coming to the next battle.<br>
                          </li>
                          <li>
                            <b>Stay calm</b>. This means in every situation. Don't get worried, stay calm and concentrate. If you stay calm you find it easier to control your actions. If you are worried or nervous or under pressure you can make hasty options and usually they aren't the best and can cost you a life, an objective or even the game.<br>
                          </li>
                          <li>
                            <b>Watch out for corners</b>. Basically if you have a corner set out like this&nbsp;<a href="http://i.imgur.com/6O2CHIi.jpg">http://i.imgur.com/6O2CHIi.jpg</a>&nbsp;then your arm or leg can pop out of it. You can get hit or shot from this so either stay a bit away or put an extra 2 blocks in front so it doesn't glitch out. Also if you see something like that watch out for an arm or a leg, you can shoot/hit it and cause damage. This can kill people and is often why people accuse others of saying 'You can hit through walls'.<br>
                          </li>
                          <li>
                            <b>Set your sprint key to something you're comfortable with</b>. Seriously, the amount&nbsp;people who &nbsp;complain about having a tired pinky cos they're pressing left control down all the time is too damn high. Mess around and set it to something you're comfortable with, be it R, a button on your mouse, a number etc. It helps a lot and you can keep it up for a long time.<br>
                          </li>
                          <li>
                            <b>Mess around with Minecraft</b>. Find something you're comfortable with all round. FOV, View bobbing on and off, your sensitivity (also check the dpi on your mouse and put it to something you're comfortable with (if that is possible on your mouse)). I mean I use Normal FOV, view bobbing off and 128% sensitivity with 1800 on my mouse dpi. People use a whole lot of different variations.
                          </li>
                          <li>
                            <b>Don't follow others, make your own path</b>. OK I know this contradicts the pvp guide but you need to know stuff in order to find out what you're good at. Don't follow someone like HattoWolf and whilst you may be good at one thing, you're bad at another. Combine things, test things, it is what every 'pro' does.&nbsp;<br>
                          </li>
                          <li>
                            <b>Be unpredictable whilst being able to predict others</b>. This all comes from experience and practice but allows you to get the edge over others before the fight even starts.<br>
                          </li>
                          <li>
                            <b>Inventory layout</b>. Get yourself an inventory layout that suits you best. It can often help in the tight situations so you can get your items quicker. (Also when getting an item, use the soup method. This is shift and left click and right click (basically like shifting whilst block hitting) instead of normal shift left click. It is just quicker)<br>
                          </li>
                          <li>
                            <b>Learn to hotkey</b>. Hot keying allows you to get to your item faster without having to scroll to it. It is a lot faster and allows for a quick item switch. Hot keying is basically pressing the numbers on your keyboard (or mouse if mmo) and getting to that item.<br>
                          </li>
                          <li>
                            <b>Keep warm</b>. By keeping warm, you keep good blood flow to your fingers and hands. If not then they slow down because blood is needed else where, this slows down your reflex and clicking ability and makes you a lot slower.<br>
                          </li>
                          <li>
                            <b>Listen to music</b>. It allows you to relax, concentrate and shut off from outside irritations. I enjoy it and I find I fight better whilst listening to it.<br>
                          </li>
                          <li>
                            <b>Stick in near/close to a group</b>. Don't stay in a group as it isn't that fun but if you stay close to a group they will usually help you out in situations where you'll probably die.<br>
                          </li>
                          <li>
                            <b>Talk to friends</b>. By going on TS, Mumble or Skype with your friends you get more relaxed and find yourself doing more daring things. It just allows you to shut off a bit whilst having a good time and you can use teamwork to help you.<br>
                          </li>
                          <li>
                            <b>Get good at parkour</b>. By getting better at parkour, you understand terrain better. By putting parkour and pvp ability together, you become a master of terrain. You will be able to analyse situations quickly and bring your enemy into situations where you can kill them.<br>
                          </li>
                          <li>
                            <b>Mods</b>. Mods such as optifine, allow for a fps boost and allow you to change settings to allow for an optimal performance. Mods such as Reis Minimap allow for analysing terrain quickly. Mods such as Status effect and armor effect allow for quick checking of item status and any potion effects you may have. Also BSM helps by not having to tire out your finger (only use if server allows it) but 1.7 also has numerous advantages over BSM (search a video on it) e.g.&nbsp;<a href="https://www.youtube.com/watch?v=h5DoWh67AjE">https://www.youtube.com/watch?v=h5DoWh67AjE</a>&nbsp;Basically mods are a lot more helpful than first thought.<br>
                          </li>
                          <li>
                            <b>Resource packs</b>. By finding a resource pack (mainly pvp) that you like you find yourself doing better, I don't know why but you just do.<br>
                          </li>
                          <li>
                            <b>Good mouse and keyboard</b>. I would advise against this if you're just gonna play minecraft but if not then get a better keyboard and mouse. It allows for easier control and you find yourself playing a lot better. (Also if you can a new computer helps A LOT too)<br>
                          </li>
                          <li>
                            <b>Stay hydrated</b>. By staying hydrated you allow more activity in your brain which allows you to be faster all round.<br>
                          </li>
                          <li>
                            <b>Confidence</b>. Believe that you can win. Confidence is a huge factor in pvp. If you think you're gonna lose, then you'll most likely lose, if you convince yourself you're gonna win and you're going to obliterate/dominate them then the chances of winning are high. NOBODY and I mean NOBODY is bad at everything in Minecraft, to get good at everything you need patience, practice and experience.&nbsp;Believe in yourself!<br>
                          </li>
                        </ul>
