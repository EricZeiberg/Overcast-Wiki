                <h2>Bow Guide (Written by Mdahl)</h2>
                <hr>
                <ol>
                  <p>
                    <li> <strong> You don't have to pull it back all the way.</strong> Though you deal critical damage by pulling it back all the way, some situations warrant a faster fire rate simply to keep your opponent knocked back and at a distance. It's a good idea to simply spam arrows at a charging enemy wielding a sword to hold him in place until you feel it's safe to deal a finishing blow. </li>
                  </p>
                  <p>
                    <li><strong>Walls don't always help.</strong> It seems counterintuitive, but walls with battlements or firing ports make it easier for enemies to expect where you will pop up, easier to sight in, and harder for you to have a wide view of the battlefield. Visibility and mobility is key to using a bow effectively; you only need to build walls if you know where the enemy is and you are under fire. You should always try to be mobile as well; try moving unpredictably after shots.</li>
                  </p>
                  <p>
                    <li><strong>Fire at a high angle.</strong> Though this takes some practice, enemies behind cover can be sussed out by firing at a high arc—you don't pull back as much on the arrow and lob death from above. Following that theme, in a map like Sand Wars, a good tactic is to shoot up the floor with arrows; when the sand falls, the arrows dislodge themselves and you can sometimes get a lucky kill. This arrow-falling tactic also applies to blocks covered with arrows that are broken—this works really well for enemies tunneling under in Battle of Lyndannise: If you block up the hole that the enemies exit out of, and cover it in arrows, when the opponent breaks the block to attack you he will immediately be killed by the dislodged arrows. Furthermore, if you have really good timing, you can do the same on other maps like Race for Victory etc. Pillar up above the enemy, make a skypath, and shoot up a (optimally glass) block full of arrows. When an enemy races under you, you can break the block and drop a deadly arrow bomb from above.</li>
                  </p>
                  <p>
                    <li><strong>Know your limits.</strong> Fully charged arrows do minimal damage to foes with full diamond armour, and take a long time to kill even iron armour, so sometimes shooting at them, especially when you're trying to sneak around, can arouse investigation in the area and give you away. However, even if they don't damage that much, they are good for holding enemies back at a range with their knockback. Furthermore, arrows are invaluable in dislodging opponents contstructing cannons, bridges, etc. and disrupting their activities. Arrow fire in general does produce a hesitant psychological effect on the enemy.</li>
                  </p>
                  <p>
                    <li><strong>Play as a team.</strong> Seems like a no-brainer, but I see a lot of people not paying attention to who exactly they are shooting at and their significance–Always provide covering fire to people who are charging in to attack with a sword, and always take care to select vulnerable or high-value targets. Remember to make power shots when picking off unarmoured or exposed targets: jumping or sprinting forward while shooting a high-powered shot can give your arrow a boost. Always keep in mind that with every enemy you kill, you reduce their strength to attack your allies, so it's usually a better idea to kill three unarmoured aggressors, who will land some hits regardless of their survivability, than it is to try and wear down one heavily armoured foe.</li>
                  </p>
                </ol>
